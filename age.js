let firstName = 'John';
let age = 13;

if (age < 13) {
    console.log(firstName + ' is a a boy!');
} else if (age < 20) {
    console.log(firstName + ' is a a teenager!');
} else if (age < 30) {
    console.log(firstName + ' is a a young man!');
} else {
    console.log(firstName + ' is a man!')
}